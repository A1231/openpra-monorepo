/**
 * Represents the additional properties of the icon - especially for elliptical svg generation.
 */
export interface IAdditionalIconProps {
  /**
   * @param cx - center x
   */
  cx: string;
  /**
   * @param cy - center y
   */
  cy: string;
  /**
   * @param rx - radius x
   */
  rx: string;
  /**
   * @param ry - radius y
   */
  ry: string;
}
